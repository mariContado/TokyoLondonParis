
<h1>Tokyo</h1>
<img src="assets/img/tokyo.jpg" class="img-fluid">
<p>Tokyo is the capital city of Japan and one of the largest metropolitan areas in the world. Known for its blend of modern technology, vibrant culture, and rich history, Tokyo is a global center for finance, innovation, and entertainment.</p>
<p>Originally a small fishing village called Edo, the city grew rapidly after becoming the seat of the Tokugawa shogunate. It was renamed Tokyo, meaning "Eastern Capital," in 1868 when it became the capital of Japan.</p>


