
<h1>Paris</h1>
<img src="assets/img/paris.jpg" class="img-fluid">
<p>Paris is the capital city of France. It is one of the most visited and culturally influential cities in the world, known for its art, fashion, gastronomy, and iconic landmarks.</p>
<p>Located along the River Seine, Paris has a rich history dating back to ancient times. Originally settled by the Parisii tribe, the city later flourished under Roman rule and eventually became the political and cultural heart of France.</p>



